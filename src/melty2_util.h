#ifndef MELTY2_MELTY2_UTIL_H_
#define MELTY2_MELTY2_UTIL_H_

#define melty2_kernel(v, a, b) do { \
    v[0] += a; \
    v[1] ^= a; \
    v[2] += b; \
    v[3] ^= b; \
    v[0] = v[0] << 7 | v[0] >> 25; \
    v[1] -= v[0]; \
    v[2] ^= v[1]; \
    v[2] = v[2] << 9 | v[2] >> 23; \
    v[3] += v[2]; \
    v[4] ^= v[3]; \
    v[4] = v[4] << 13 | v[4] >> 19; \
    v[5] += v[4]; \
    v[0] ^= v[5]; \
    v[0] = v[0] << 18 | v[0] >> 14; \
    v[1] += v[0]; \
    v[2] ^= v[1]; \
    v[2] = v[2] << 7 | v[2] >> 25; \
    v[3] -= v[2]; \
    v[4] ^= v[3]; \
    v[4] = v[4] << 9 | v[4] >> 23; \
    v[5] += v[4]; \
    v[0] ^= v[5]; \
    v[0] = v[0] << 13 | v[0] >> 19; \
    v[1] += v[0]; \
    v[2] ^= v[1]; \
    v[2] = v[2] << 18 | v[2] >> 14; \
    v[3] += v[2]; \
    v[4] ^= v[3]; \
    v[4] = v[4] << 12 | v[4] >> 20; \
    v[5] -= v[4]; \
    v[0] ^= v[5]; \
} while (0)

#endif  // MELTY2_MELTY2_UTIL_H_

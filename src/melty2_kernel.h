#ifndef MELTY2_MELTY2_KERNEL_H_
#define MELTY2_MELTY2_KERNEL_H_

#define melty2_kernel(v, a, b) ( \
    (v)[0] ^= (a), \
    (v)[1] += (b), \
    (v)[2] ^= (b), \
    (v)[3] += (a), \
    (v)[1] ^= (v)[0], \
    (v)[1] = (v)[1] << 13 | (v)[1] >> 19, \
    (v)[2] += (v)[1], \
    (v)[3] ^= (v)[2], \
    (v)[3] = (v)[3] << 18 | (v)[3] >> 14, \
    (v)[4] += (v)[3], \
    (v)[5] ^= (v)[4], \
    (v)[5] = (v)[5] << 7 | (v)[5] >> 25, \
    (v)[0] += (v)[5], \
    (v)[1] ^= (v)[0], \
    (v)[1] = (v)[1] << 9  | (v)[1] >> 23, \
    (v)[2] += (v)[1], \
    (v)[3] ^= (v)[2], \
    (v)[3] = (v)[3] << 13 | (v)[3] >> 19, \
    (v)[4] += (v)[3], \
    (v)[5] ^= (v)[4], \
    (v)[5] = (v)[5] << 18 | (v)[5] >> 14, \
    (v)[0] += (v)[5], \
    (v)[1] ^= (v)[0], \
    (v)[1] = (v)[1] << 7 | (v)[1] >> 25, \
    (v)[2] += (v)[1], \
    (v)[3] ^= (v)[2], \
    (v)[3] = (v)[3] << 9 | (v)[3] >> 23, \
    (v)[0] += (v)[3], \
    (v)[1] ^= (v)[0], \
    (v)[1] = (v)[1] << 13 | (v)[1] >> 19, \
    (v)[2] += (v)[1], \
    (v)[3] ^= (v)[2], \
    (v)[3] = (v)[3] << 18 | (v)[3] >> 14, \
    (v)[0] += (v)[3], \
    (v)[2] ^= (v)[0] \
)

#endif  // MELTY2_MELTY2_KERNEL_H_

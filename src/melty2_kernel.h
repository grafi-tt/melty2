#ifndef MELTY2_MELTY2_KERNEL_H_
#define MELTY2_MELTY2_KERNEL_H_

#define melty2_kernel(v, a, b) ( \
    (v)[0] += (a), \
    (v)[1] ^= (a), \
    (v)[2] += (b), \
    (v)[3] ^= (b), \
    (v)[0] = (v)[0] << 7 | (v)[0] >> 25, \
    (v)[1] -= (v)[0], \
    (v)[2] ^= (v)[1], \
    (v)[2] = (v)[2] << 9 | (v)[2] >> 23, \
    (v)[3] += (v)[2], \
    (v)[4] ^= (v)[3], \
    (v)[4] = (v)[4] << 13 | (v)[4] >> 19, \
    (v)[5] += (v)[4], \
    (v)[0] ^= (v)[5], \
    (v)[0] = (v)[0] << 18 | (v)[0] >> 14, \
    (v)[1] += (v)[0], \
    (v)[2] ^= (v)[1], \
    (v)[2] = (v)[2] << 11 | (v)[2] >> 21, \
    (v)[3] -= (v)[2], \
    (v)[4] ^= (v)[3], \
    (v)[4] = (v)[4] << 7 | (v)[4] >> 25, \
    (v)[5] += (v)[4], \
    (v)[0] ^= (v)[5], \
    (v)[0] = (v)[0] << 9 | (v)[0] >> 23, \
    (v)[1] += (v)[0], \
    (v)[2] ^= (v)[1], \
    (v)[2] = (v)[2] << 13 | (v)[2] >> 19, \
    (v)[3] += (v)[2], \
    (v)[4] ^= (v)[3], \
    (v)[4] = (v)[4] << 18 | (v)[4] >> 14, \
    (v)[5] -= (v)[4], \
    (v)[0] ^= (v)[5], \
    (v)[0] ^ (v)[3] \
)

#endif  // MELTY2_MELTY2_KERNEL_H_
